    public void userLoad() {
        int count;
        try {
            Connection con = ConnectionProvider.getConnection();
            pst = con.prepareStatement("select * from scores");
            rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            count = rsd.getColumnCount();
            d = (DefaultTableModel) tblScore.getModel();
            d.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= count; i++) {
                    v2.add(rs.getString("match_id"));
                    v2.add(rs.getString("team_id"));
                    v2.add(rs.getString("scores"));
                    v2.add(rs.getString("result"));
                }
                d.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddUpdateDeleteScore.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

